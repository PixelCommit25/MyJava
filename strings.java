public class strings {
    public static void main(String args[])
    {
        String r=new String("rosario");
        System.out.println(r);
        System.out.println("the length string "+r+" is :"+r.length());
        System.out.println("the string in lower case is : "+r.toLowerCase());
        System.out.println("the string in upper case is : "+r.toUpperCase());
        System.out.println(r.substring(0  ));
        System.out.println(r.substring(0,3) );
    }
    
}
